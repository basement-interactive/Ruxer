/// Case-insensitive shortcode lookup. Returns the canonical shortcode + unicode.
export function resolveShortcodeCi(name: string): readonly [string, string] | undefined {
  const lower = name.toLowerCase();
  return EMOJI_TABLE.find(([code]) => code === lower);
}

/// Return up to `limit` shortcodes that start with `prefix` (case-insensitive).
export function searchShortcodes(prefix: string, limit: number): Array<readonly [string, string]> {
  if (!prefix) return EMOJI_TABLE.slice(0, limit);
  const lower = prefix.toLowerCase();
  return EMOJI_TABLE
    .filter(([code]) => code.startsWith(lower))
    .slice(0, limit);
}